package encapsulamento;

public class Pessoa {
    public String nome;
    public int idade;
    public double peso;

    public Pessoa() {
        this.peso = peso;
        this.idade = idade;
        this.nome = nome;
    }

    public int getIdade() {
        return idade;
    }

    public void setIdade(int idade) {
        this.idade = idade;
    }

    public double getPeso() {
        return peso;
    }

    public void setPeso(double peso) {
        this.peso = peso;
    }
}
